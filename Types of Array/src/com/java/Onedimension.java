package com.java;

public class Onedimension 
{
		public static void main(String args[])
		{    
			//declaration
			int[] a=new int[3];  
			//initialization 
			a[0]=10; 
			a[1]=20;  
			a[2]=30;  
			//printing array  
			System.out.println("One dimensional array elements are");    
			System.out.println(a[0]);    
			System.out.println(a[1]);    
			System.out.println(a[2]);    
		}
}
